    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        HttpSession ses=request.getSession();
        Account acc=(Account)ses.getAttribute("account");
        
        int articleId=Integer.parseInt(ses.getAttribute("artId").toString());
        String comment= request.getParameter("comment");
        int star=Integer.parseInt(request.getParameter("star").toString());
        dao.addComment(acc.getId(), articleId, star, comment, 0);
        //request.getRequestDispatcher("Articles?id="+articleId).forward(request, response);
        response.sendRedirect("ArticlesPage?id="+articleId);
    }

